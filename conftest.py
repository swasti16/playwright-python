def collect_artifacts(page: Page, context: BrowserContext, request, browser_name: str, worker_id: str):
    failed = (hasattr(request.node, "rep_call") and request.node.rep_call.failed)

    if failed:
        screenshot = Settings.get_screenshot_path(
            browser_name=browser_name,
            worker_id=worker_id,
            test_name=request.node.name,
        )
        try:
            page.screenshot(path=str(screenshot), full_page=True)
            allure.attach.file(str(screenshot), name="Failure Screenshot",
                               attachment_type=allure.attachment_type.PNG)
        except Exception as e:
            print(e)

        trace = Settings.get_trace_path(browser_name=browser_name, worker_id=worker_id, test_name=request.node.name)
        try:
            context.tracing.stop(path=str(trace))
        except Exception as e:
            print(e)
    else:
        context.tracing.stop()


