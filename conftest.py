def credentials(worker_id):
    return Settings.get_worker_credentials(worker_id)


def create_storage_state(browser: Browser, auth_file: str, credentials):
    try:
        context = browser.new_context()
        page = context.new_page()
        login_page = LoginPage(page)
        login_page.navigate(Settings.BASE_URL)
        login_page.get_by_role("link", name="Sign in").click()
        login_page.login_and_wait_for_profile(credentials["username"], credentials["password"])
        login_page.to_be_visible(login_page.user_button(credentials["name"]), timeout=30000)
        context.storage_state(path=auth_file)
        if not Path(auth_file).is_file():
            raise RuntimeError(
                f"Storage state file '{auth_file}' was not created."
            )
    except Exception:
        try:
            alert = page.get_by_role("alert")
            if alert.is_visible(timeout=1000):
                print(alert.first.text_content())
        except Exception:
            pass
        raise
    finally:
        page.close()
        context.close()


@pytest.fixture(scope="session", autouse=True)
def create_auth_state(browser: Browser, auth_file: str, credentials):
    for attempt in range(1, Settings.AUTH_RETRY_COUNT + 1):
        try:
            print(f"\nCreating auth state ({attempt}/{Settings.AUTH_RETRY_COUNT})...")
            create_storage_state(browser, auth_file, credentials)
            print("Auth state created successfully.")
            return
        except Exception as e:
            print(f"Attempt {attempt} failed: {e}")
            if attempt == Settings.AUTH_RETRY_COUNT:
                raise
            print("Retrying authentication...\n")


